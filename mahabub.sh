# Global variables for tracking background jobs
declare -a BG_PIDS=()
CURRENT_PHASE=""
INTERRUPTED=false

# Function to handle Ctrl+C gracefully
smart_interrupt() {
    echo -e "\n${YELLOW}[!] Ctrl+C detected!${NC}"
    echo -e "${YELLOW}[вҶ’] Current phase: $CURRENT_PHASE${NC}"
    echo -e "${YELLOW}[вҶ’] Stopping current tools only...${NC}"

    INTERRUPTED=true

    # Kill only the tools running in current phase (background jobs)
    for pid in ${BG_PIDS[@]}; do
        if kill -0 $pid 2>/dev/null; then
            echo -e "${RED}[вҶ’] Stopping PID: $pid${NC}"
            kill $pid 2>/dev/null
            kill -9 $pid 2>/dev/null
        fi
    done

    # Clear the PID array
    BG_PIDS=()

    echo -e "${GREEN}[вң“] Current tools stopped. Moving to next phase...${NC}"
    echo -e "${CYAN}в”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғв”Ғ${NC}"
}

# Set the trap
trap smart_interrupt SIGINT SIGTERM

# Function to add PID to tracking
track_pid() {
    BG_PIDS+=($1)
}

# Function to wait for all tracked jobs
wait_with_check() {
    while true; do
        local running=false
        local new_pids=()

        for pid in ${BG_PIDS[@]}; do
            if kill -0 $pid 2>/dev/null; then
                running=true
                new_pids+=($pid)
            fi
        done

        BG_PIDS=("${new_pids[@]}")

        if [ "$running" = false ]; then
            break
        fi
        sleep 1
    done
}

